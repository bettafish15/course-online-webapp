package com.example.swp490_g25_sse.model;

import java.util.List;
import java.util.Set;
import javax.persistence.CascadeType;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.OneToMany;
import javax.persistence.OneToOne;
import javax.persistence.Table;
import javax.persistence.UniqueConstraint;

/**
 *
 * @author bettafish15
 */
@Entity()
@Table(name = "students", uniqueConstraints = @UniqueConstraint(columnNames = "user_id"))
public class Student {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @OneToOne(cascade = CascadeType.ALL)
    @JoinColumn(name = "user_id", referencedColumnName = "id")
    private User user;

    @OneToMany(mappedBy = "student", cascade = CascadeType.ALL)
    private List<Question> questions;

    @OneToMany(mappedBy = "student", cascade = CascadeType.ALL)
    private Set<StudentCourseEnrollment> courses;

    public Student() {
    }

    public Student(User user) {
        this.user = user;
    }

    public Long getId() {
        return this.id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public User getUser() {
        return this.user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Set<StudentCourseEnrollment> getCourses() {
        return this.courses;
    }

    public void setCourses(Set<StudentCourseEnrollment> courses) {
        this.courses = courses;
    }
}
